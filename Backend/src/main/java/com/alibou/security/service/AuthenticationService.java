    public AuthenticationResponse register(RegisterRequest request) {
        boolean userExists = repository
                .findByEmail(request.getEmail())
                .isPresent();
        if (userExists) {


            throw new IllegalStateException("NameRespense deja pris ");
        }

        var user = User.builder()
                .name(request.getName())
                .email(request.getEmail())
                .password(passwordEncoder.encode(request.getPassword()))
                .role(request.getRole())



                .build();

         repository.save(user);
        String toke = UUID.randomUUID().toString();
        ConfirmationToken confirmationToken = new ConfirmationToken(
                toke,
                LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(15),
                user
        );

        confirmationTokenService.saveConfirmationToken(
                confirmationToken);
        String link = "http://localhost:8080/api/auth/token?token=" + toke;
        emailSender.send(
                request.getEmail(),
                buildEmail(request.getName(), link),"Confirmer votre NameRespense");
        var jwtToken = jwtService.generateToken(user);


        return AuthenticationResponse.builder()
                .token(jwtToken).role(request.getRole()).toke(toke)
                .build();
    }
