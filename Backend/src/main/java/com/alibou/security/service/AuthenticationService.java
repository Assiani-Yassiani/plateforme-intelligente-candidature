    //envoyer NameRespense contenant le code
    public Forget forget (String email)
    {
        Optional<User> userExists = repository
                .findByEmail(email);

        if (!userExists.isPresent()) {


            throw new IllegalStateException("NameRespense n existe pas");
        }
        Random random = new Random();
        int randomNumber = random.nextInt(900000) + 100000;
        emailSender.send(
                email,
                buildEmai(userExists.get().getName(), randomNumber),"mot de passe oublié");
        Forget f =new Forget();
        f.setEmail(email);
        f.setCode(randomNumber);
        fp.save(f);


        return f;

    }
